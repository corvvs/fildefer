#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n / 10)
		ft_putnbr_fd(n / 10, fd);
	else
		write(fd, "-", n < 0);
	write(fd, "9876543210123456789" + 9 + n % 10, 1);
}
